export function isStopWord(token: string): boolean {
  const normalized = token.toLowerCase().replace(/['']/g, '').replace(/[.,;:!?]+$/g, '')
  return ENGLISH_STOP_WORDS.has(normalized)
}

/** Remove stop words from a token array before scoring or mismatch checks. */
export function filterStopWordTokens(tokens: string[]): string[] {
  return tokens.filter((token) => token.length >= 2 && !isStopWord(token))
}

/** Rebuild a phrase with conversational tokens removed. */
export function phraseWithoutStopWords(phrase: string): string {
  return filterStopWordTokens(tokenize(phrase)).join(' ')
}

export function isPureStopWordPhrase(phrase: string): boolean {
  const tokens = tokenize(phrase)
  return tokens.length === 0 || tokens.every((token) => isStopWord(token))
}

export function tokenize(text: string): string[] {
  return (text.toLowerCase().match(/[a-z0-9+#][a-z0-9+#./-]*/g) ?? [])
    .map((token) => token.replace(/[.,;:!?]+$/g, ''))
    .filter(Boolean)
}

/** Strip stop words from raw job description text prior to token frequency analysis. */
export function stripStopWordsFromText(text: string): string {
  return filterStopWordTokens(tokenize(text)).join(' ')
}
